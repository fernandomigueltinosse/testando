    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(!txtIdcredito.getText().equals("")){
            
              try {
                  HashMap a = new HashMap();
                    a.put("id", txtIdcredito.getText());
                  jPanel1.removeAll();
                  jPanel1.repaint();
                  jPanel1.revalidate();
                  JasperDesign reportpath = JRXmlLoader.load("src/relatorios/vendas.jrxml");
                  JasperReport jr = JasperCompileManager.compileReport(reportpath);
                  JasperPrint jp = JasperFillManager.fillReport(jr,a,conn);
                  JRViewer v = new JRViewer(jp);
                  jPanel1.setLayout(new BorderLayout());
                  jPanel1.add(v);

        } catch ( JRException e) {
            JOptionPane.showMessageDialog(null, e);
        }
        
        }else{ JOptionPane.showMessageDialog(null, "Selecione o codigo do pedido de credito"); }
    }//GEN-LAST:event_jButton1ActionPerformed

