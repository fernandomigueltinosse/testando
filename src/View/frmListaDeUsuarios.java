private void listar_usuarios(){
    String sql = "select id,usuarios,password,cargo from tbusuario where usuarios like ? limit 50";
    try {
        pst = conn.prepareStatement(sql);
        pst.setString(1, txtProcurar.getText() + "%");
        rs = pst.executeQuery();
        tblUsuarios.setModel(DbUtils.resultSetToTableModel(rs));
        
    } catch (Exception e) {
         JOptionPane.showMessageDialog(null, e);
    }
}

