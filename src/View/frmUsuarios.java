private void apagar(){
    
     int confirmar = JOptionPane.showConfirmDialog(null, "tem certeza que deseja apagar?","Atenção",JOptionPane.YES_NO_OPTION);
        if(confirmar == JOptionPane.YES_OPTION){
            
            String sql = "delete from tbusuario where id=?";
         try {
             pst = conn.prepareStatement(sql);
             pst.setString(1, txtId.getText());
             pst.executeUpdate();
             JOptionPane.showMessageDialog(null, "Usuario apagado com sucesso");
             limpar();
         } catch (Exception e) {
             JOptionPane.showMessageDialog(null, e);
           }
        }
}

