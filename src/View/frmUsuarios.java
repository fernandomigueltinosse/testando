    private void limpar(){
        txtId.setText(null);
        txtUsuario.setText(null);
        txtPassword.setText(null);
        comboCargo.setSelectedItem(null);
    }
////////////////////////////////////////////////////////////////////////////////    
    private void registar(){
        String sql = "insert into tbusuario (usuarios,password,cargo) value(?,?,?)";
        try {
            pst =conn.prepareStatement(sql);
            pst.setString(1, txtUsuario.getText());
            pst.setString(2, txtPassword.getText());
            pst.setString(3, comboCargo.getSelectedItem().toString());
            //validacao dos campos obrigatorios
            if (txtUsuario.getText().isEmpty()) {
                 JOptionPane.showMessageDialog(null, "preencha os campos obrigatorios");
                
            } else {
            
            pst.executeUpdate();
            JOptionPane.showMessageDialog(null, "Usuario adicionado com sucesso");
            limpar();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }
/////////////////////////////////////////////////////////////////////////////////////

private void actualizar(){

    String sql = "update tbusuario set usuarios=?, password=?, cargo=? where id=?";
    try {
         pst = conn.prepareStatement(sql);
         pst.setString(1, txtUsuario.getText());
         pst.setString(2, txtPassword.getText());
         pst.setString(3, comboCargo.getSelectedItem().toString());
         pst.setString(4, txtId.getText());
         pst.executeUpdate();
         JOptionPane.showMessageDialog(null, "Usuario actualizado com sucesso");
         limpar();
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, e);
    }
}
