       private void credito(){
    String sql = "SELECT SUM(valor +multa+ quebra_do_contrato+Valor_do_bem) as receita from venda WHERE DATE(data_da_operacao) BETWEEN ? and ?";
    try {
        pst = conn.prepareStatement(sql);
        pst.setString(1, data.format(txtDataInicio1.getDate()));
        pst.setString(2, data.format(txtDataFim1.getDate()));
        rs = pst.executeQuery();
      
        while(rs.next()){

        Object list[] = new Object[9];
            list[1]= rs.getDouble(1);
        lblCredito.setText(String.valueOf(list[1]));
        }
      
    } catch (Exception e) {
         JOptionPane.showMessageDialog(null, e);
    }
}
