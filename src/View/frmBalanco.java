    private void debitoEJuros(){
    String sql = "SELECT SUM(montante_aprovado), SUM(valor_total-montante_aprovado),SUM(valor_total) FROM pedido WHERE DATE(data_do_pedido) BETWEEN ? and ?";
    try {
        pst = conn.prepareStatement(sql);
        pst.setString(1, data.format(txtDataInicio.getDate()));
        pst.setString(2, data.format(txtDataFim.getDate()));
        rs = pst.executeQuery();
      
        while(rs.next()){

        Object list[] = new Object[9];
            list[1]= rs.getDouble(1);
            list[2]= rs.getDouble(2);
            list[3] = rs.getDouble(3);
         lblTotalDeJuros.setText(String.valueOf(list[2]));
        lblMontanteConcedido.setText(String.valueOf(list[1]));
        lblReceita.setText(String.valueOf(list[3]));
        
        }
      
    } catch (Exception e) {
         JOptionPane.showMessageDialog(null, e);
    }
}
